-- E-commerce multi-tenant da clínica: catálogo, pedidos, cupons, estoque reservado e carrinhos recuperáveis.

create table if not exists public.produtos_clinica (
  id uuid primary key default gen_random_uuid(),
  clinica_id uuid not null references public.clinicas(id) on delete cascade,
  nome text not null,
  sku text,
  codigo_barras text,
  categoria text,
  descricao text,
  custo numeric(12,2) not null default 0 check (custo >= 0),
  preco numeric(12,2) not null default 0 check (preco >= 0),
  estoque_atual numeric(12,3) not null default 0 check (estoque_atual >= 0),
  estoque_reservado numeric(12,3) not null default 0 check (estoque_reservado >= 0),
  estoque_minimo numeric(12,3) not null default 0 check (estoque_minimo >= 0),
  unidade text not null default 'un',
  imagem_url text,
  publicado_site boolean not null default false,
  ativo boolean not null default true,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now(),
  unique (clinica_id, id),
  unique (clinica_id, sku)
);

create index if not exists produtos_clinica_nome_idx on public.produtos_clinica (clinica_id, nome);
create index if not exists produtos_clinica_estoque_idx on public.produtos_clinica (clinica_id, ativo, estoque_atual);

