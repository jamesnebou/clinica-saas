create or replace function public.criar_pedido_loja(
  p_clinica_id uuid,
  p_cliente_id uuid,
  p_itens jsonb,
  p_nome text,
  p_telefone text,
  p_email text default null,
  p_cpf text default null,
  p_entrega_tipo text default 'retirada',
  p_frete numeric default 0,
  p_cep text default null,
  p_endereco text default null,
  p_numero_endereco text default null,
  p_complemento text default null,
  p_bairro text default null,
  p_cidade text default null,
  p_estado text default null,
  p_referencia_entrega text default null,
  p_cupom_codigo text default null,
  p_forma_pagamento text default 'UNDEFINED',
  p_reserva_minutos integer default 30,
  p_origem jsonb default '{}'::jsonb
)
returns table (pedido_id uuid, token_publico uuid, numero bigint, subtotal numeric, desconto numeric, frete numeric, total numeric)
language plpgsql
security definer
set search_path = public
as $$
declare
  v_pedido public.pedidos_clinica%rowtype;
  v_produto public.produtos_clinica%rowtype;
  v_cupom public.cupons_clinica%rowtype;
  v_item record;
  v_subtotal numeric(12,2) := 0;
  v_desconto numeric(12,2) := 0;
  v_frete numeric(12,2) := greatest(coalesce(p_frete, 0), 0);
  v_total numeric(12,2) := 0;
  v_expira_em timestamptz := now() + make_interval(mins => greatest(10, least(coalesce(p_reserva_minutos, 30), 240)));
  v_item_count integer := 0;
begin
  if p_entrega_tipo not in ('retirada', 'entrega') then
    raise exception 'Forma de entrega invalida.';
  end if;

  if coalesce(trim(p_nome), '') = '' or coalesce(trim(p_telefone), '') = '' then
    raise exception 'Nome e telefone sao obrigatorios.';
  end if;

  if jsonb_typeof(p_itens) <> 'array' or jsonb_array_length(p_itens) = 0 then
    raise exception 'O carrinho esta vazio.';
  end if;

  for v_item in
    select produto_id, sum(quantidade)::numeric(12,3) as quantidade
    from jsonb_to_recordset(p_itens) as item(produto_id uuid, quantidade numeric)
    group by produto_id
  loop
    v_item_count := v_item_count + 1;
    if v_item.quantidade <= 0 or v_item.quantidade <> trunc(v_item.quantidade) then
      raise exception 'Quantidade invalida no carrinho.';
    end if;

    select * into v_produto
    from public.produtos_clinica
    where id = v_item.produto_id
      and clinica_id = p_clinica_id
      and ativo = true
      and publicado_site = true
    for update;

    if not found then
      raise exception 'Produto indisponivel.';
    end if;

    if (v_produto.estoque_atual - v_produto.estoque_reservado) < v_item.quantidade then
      raise exception 'Estoque insuficiente para %.', v_produto.nome;
    end if;

    v_subtotal := v_subtotal + round(v_produto.preco * v_item.quantidade, 2);
  end loop;

  if v_item_count = 0 then
    raise exception 'O carrinho esta vazio.';
  end if;

  if coalesce(trim(p_cupom_codigo), '') <> '' then
    select * into v_cupom
    from public.cupons_clinica
    where clinica_id = p_clinica_id
      and lower(codigo) = lower(trim(p_cupom_codigo))
      and ativo = true
      and (inicia_em is null or inicia_em <= now())
      and (termina_em is null or termina_em >= now())
    for update;

    if not found then
      raise exception 'Cupom invalido ou expirado.';
    end if;
    if v_subtotal < v_cupom.pedido_minimo then
      raise exception 'Este cupom exige um pedido minimo de R$ %.', v_cupom.pedido_minimo;
    end if;
    if v_cupom.limite_usos is not null and v_cupom.usos >= v_cupom.limite_usos then
      raise exception 'O limite de uso deste cupom foi atingido.';
    end if;

    if v_cupom.tipo = 'percentual' then
      v_desconto := round(v_subtotal * (v_cupom.valor / 100), 2);
      if v_cupom.desconto_maximo is not null then
        v_desconto := least(v_desconto, v_cupom.desconto_maximo);
      end if;
    else
      v_desconto := least(v_cupom.valor, v_subtotal);
    end if;
  end if;

  v_total := greatest(round(v_subtotal - v_desconto + v_frete, 2), 0);

  insert into public.pedidos_clinica (
    clinica_id, cliente_id, cupom_id, nome_cliente, telefone_cliente, email_cliente, cpf_cliente,
    entrega_tipo, cep, endereco, numero_endereco, complemento, bairro, cidade, estado, referencia_entrega,
    subtotal, desconto, frete, total, cupom_codigo, forma_pagamento, expiracao_reserva_em, origem
  ) values (
    p_clinica_id, p_cliente_id, v_cupom.id, trim(p_nome), trim(p_telefone), nullif(trim(p_email), ''), nullif(trim(p_cpf), ''),
    p_entrega_tipo, nullif(trim(p_cep), ''), nullif(trim(p_endereco), ''), nullif(trim(p_numero_endereco), ''),
    nullif(trim(p_complemento), ''), nullif(trim(p_bairro), ''), nullif(trim(p_cidade), ''), nullif(trim(p_estado), ''),
    nullif(trim(p_referencia_entrega), ''), v_subtotal, v_desconto, v_frete, v_total, v_cupom.codigo,
    coalesce(nullif(trim(p_forma_pagamento), ''), 'UNDEFINED'), v_expira_em, coalesce(p_origem, '{}'::jsonb)
  ) returning * into v_pedido;

  for v_item in
    select produto_id, sum(quantidade)::numeric(12,3) as quantidade
    from jsonb_to_recordset(p_itens) as item(produto_id uuid, quantidade numeric)
    group by produto_id
  loop
    select * into v_produto
    from public.produtos_clinica
    where id = v_item.produto_id and clinica_id = p_clinica_id;

    update public.produtos_clinica
    set estoque_reservado = estoque_reservado + v_item.quantidade
    where id = v_produto.id and clinica_id = p_clinica_id;

    insert into public.pedido_itens_clinica (
      clinica_id, pedido_id, produto_id, nome_produto, sku, imagem_url,
      quantidade, valor_unitario, total
    ) values (
      p_clinica_id, v_pedido.id, v_produto.id, v_produto.nome, v_produto.sku, v_produto.imagem_url,
      v_item.quantidade, v_produto.preco, round(v_produto.preco * v_item.quantidade, 2)
    );

    insert into public.estoque_reservas_clinica (
      clinica_id, pedido_id, produto_id, quantidade, expira_em
    ) values (
      p_clinica_id, v_pedido.id, v_produto.id, v_item.quantidade, v_expira_em
    );

    insert into public.estoque_movimentos_clinica (
      clinica_id, produto_id, pedido_id, tipo, quantidade,
      estoque_anterior, estoque_posterior, observacoes
    ) values (
      p_clinica_id, v_produto.id, v_pedido.id, 'reserva', 0,
      v_produto.estoque_atual, v_produto.estoque_atual, 'Reserva temporaria do pedido online.'
    );
  end loop;

  if v_cupom.id is not null then
    update public.cupons_clinica set usos = usos + 1 where id = v_cupom.id;
  end if;

  return query select v_pedido.id, v_pedido.token_publico, v_pedido.numero,
    v_subtotal, v_desconto, v_frete, v_total;
end;
$$;

create or replace function public.cancelar_pedido_loja(p_pedido_id uuid, p_motivo text default null)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_pedido public.pedidos_clinica%rowtype;
  v_reserva public.estoque_reservas_clinica%rowtype;
  v_estoque numeric(12,3);
begin
  select * into v_pedido from public.pedidos_clinica where id = p_pedido_id for update;
  if not found then return false; end if;
  if v_pedido.status in ('cancelado', 'estornado') then return true; end if;
  if v_pedido.pagamento_status = 'pago' then
    raise exception 'Pedido pago deve ser estornado, nao cancelado.';
  end if;

  for v_reserva in
    select * from public.estoque_reservas_clinica
    where pedido_id = p_pedido_id and status = 'ativa'
    for update
  loop
    select estoque_atual into v_estoque
    from public.produtos_clinica
    where id = v_reserva.produto_id and clinica_id = v_reserva.clinica_id
    for update;

    update public.produtos_clinica
    set estoque_reservado = greatest(estoque_reservado - v_reserva.quantidade, 0)
    where id = v_reserva.produto_id and clinica_id = v_reserva.clinica_id;

    update public.estoque_reservas_clinica
    set status = case when expira_em <= now() then 'expirada' else 'liberada' end,
        finalizada_em = now()
    where id = v_reserva.id;

    insert into public.estoque_movimentos_clinica (
      clinica_id, produto_id, pedido_id, tipo, quantidade,
      estoque_anterior, estoque_posterior, observacoes
    ) values (
      v_reserva.clinica_id, v_reserva.produto_id, p_pedido_id, 'liberacao', 0,
      v_estoque, v_estoque, coalesce(p_motivo, 'Reserva liberada.')
    );
  end loop;

  if v_pedido.cupom_id is not null then
    update public.cupons_clinica set usos = greatest(usos - 1, 0) where id = v_pedido.cupom_id;
  end if;

  update public.pedidos_clinica
  set status = 'cancelado', pagamento_status = 'cancelado',
      observacoes = concat_ws(E'\n', observacoes, p_motivo)
  where id = p_pedido_id;
  return true;
end;
$$;

create or replace function public.confirmar_pagamento_pedido_loja(
  p_pedido_id uuid,
  p_asaas_payment_id text default null,
  p_payload jsonb default '{}'::jsonb,
  p_pago_em timestamptz default now()
)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_pedido public.pedidos_clinica%rowtype;
  v_reserva public.estoque_reservas_clinica%rowtype;
  v_produto public.produtos_clinica%rowtype;
begin
  select * into v_pedido from public.pedidos_clinica where id = p_pedido_id for update;
  if not found then return false; end if;
  if v_pedido.pagamento_status = 'pago' then return true; end if;
  if v_pedido.status in ('cancelado', 'estornado') then
    raise exception 'Pedido cancelado nao pode ser confirmado.';
  end if;

  for v_reserva in
    select * from public.estoque_reservas_clinica
    where pedido_id = p_pedido_id and status = 'ativa'
    for update
  loop
    select * into v_produto
    from public.produtos_clinica
    where id = v_reserva.produto_id and clinica_id = v_reserva.clinica_id
    for update;

    if v_produto.estoque_atual < v_reserva.quantidade then
      raise exception 'Estoque inconsistente para confirmar o pedido.';
    end if;

    update public.produtos_clinica
    set estoque_atual = estoque_atual - v_reserva.quantidade,
        estoque_reservado = greatest(estoque_reservado - v_reserva.quantidade, 0)
    where id = v_produto.id and clinica_id = v_produto.clinica_id;

    update public.estoque_reservas_clinica
    set status = 'convertida', finalizada_em = now()
    where id = v_reserva.id;

    insert into public.estoque_movimentos_clinica (
      clinica_id, produto_id, pedido_id, tipo, quantidade,
      estoque_anterior, estoque_posterior, observacoes
    ) values (
      v_produto.clinica_id, v_produto.id, p_pedido_id, 'venda', -v_reserva.quantidade,
      v_produto.estoque_atual, v_produto.estoque_atual - v_reserva.quantidade,
      'Venda confirmada pelo pagamento do pedido online.'
    );
  end loop;

  update public.pedidos_clinica
  set status = 'em_separacao', pagamento_status = 'pago', pago_em = coalesce(p_pago_em, now()),
      asaas_payment_id = coalesce(p_asaas_payment_id, asaas_payment_id),
      payload_pagamento = coalesce(p_payload, '{}'::jsonb)
  where id = p_pedido_id;
  return true;
end;
$$;

create or replace function public.estornar_pedido_loja(p_pedido_id uuid, p_motivo text default null)
returns boolean
language plpgsql
security definer
set search_path = public
as $$
declare
  v_pedido public.pedidos_clinica%rowtype;
  v_item public.pedido_itens_clinica%rowtype;
  v_estoque numeric(12,3);
begin
  select * into v_pedido from public.pedidos_clinica where id = p_pedido_id for update;
  if not found then return false; end if;
  if v_pedido.status = 'estornado' then return true; end if;
  if v_pedido.pagamento_status <> 'pago' then
    raise exception 'Somente pedidos pagos podem ser estornados.';
  end if;

  for v_item in select * from public.pedido_itens_clinica where pedido_id = p_pedido_id
  loop
    select estoque_atual into v_estoque
    from public.produtos_clinica
    where id = v_item.produto_id and clinica_id = v_item.clinica_id
    for update;

    update public.produtos_clinica
    set estoque_atual = estoque_atual + v_item.quantidade
    where id = v_item.produto_id and clinica_id = v_item.clinica_id;

    insert into public.estoque_movimentos_clinica (
      clinica_id, produto_id, pedido_id, tipo, quantidade,
      estoque_anterior, estoque_posterior, observacoes
    ) values (
      v_item.clinica_id, v_item.produto_id, p_pedido_id, 'estorno', v_item.quantidade,
      v_estoque, v_estoque + v_item.quantidade, coalesce(p_motivo, 'Estorno do pedido online.')
    );
  end loop;

  if v_pedido.cupom_id is not null then
    update public.cupons_clinica set usos = greatest(usos - 1, 0) where id = v_pedido.cupom_id;
  end if;

  update public.pedidos_clinica
  set status = 'estornado', pagamento_status = 'estornado',
      observacoes = concat_ws(E'\n', observacoes, p_motivo)
  where id = p_pedido_id;
  return true;
end;
$$;

create or replace function public.expirar_pedidos_loja()
returns integer
language plpgsql
security definer
set search_path = public
as $$
declare
  v_pedido record;
  v_total integer := 0;
begin
  for v_pedido in
    select id from public.pedidos_clinica
    where status = 'aguardando_pagamento'
      and pagamento_status = 'pendente'
      and expiracao_reserva_em < now()
    for update skip locked
  loop
    perform public.cancelar_pedido_loja(v_pedido.id, 'Reserva expirada automaticamente.');
    v_total := v_total + 1;
  end loop;
  return v_total;
end;
$$;

